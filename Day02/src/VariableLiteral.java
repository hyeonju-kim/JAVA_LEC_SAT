/*리터럴(escape sequence)
 * 
 */
public class VariableLiteral {
	public static void main(String[] args) {
		
		// 1. 문자열 나는 "김현주" 입니다.
		System.out.println("나는 \"김현주\" 입니다.");
		System.out.println("나는 '김현주' 입니다.");
		
		// 2. * 3개를 찍는데 7칸씩 띄워서 출력하고싶을떄
		// *       *       *
		System.out.println("*       *       *");
		System.out.println("*\t*\t*");
		
		
		
		
		// 3. * 3개를 찍는데 밑으로 찍고 시프다..
		System.out.println("*");
		System.out.println("*");
		System.out.println("*");
		
		System.out.println("*\n*\n*");
		
		// \r\n r은 return 
		
		
		
	}
}
